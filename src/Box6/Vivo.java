package Box6;

public class Vivo {
	public static void main(String[] args) {
		System.out.println("main start...[]string");
		main(35.3);
		System.out.println("----------");
		main(10,20);
		
		System.out.println("main end ....[]string");
	}
	
	public static void main(int args) {
		System.out.println("main start...int");
		
		System.out.println("main end ....int");
	}
	
	public static void main(double  args) {
		System.out.println("main start...double");
		
		System.out.println("main end ....double");
	}
	
	public static void main(int a,int b) {
		System.out.println("main start...int a,n");
		
		System.out.println("main end ...int a,b");
	}

}
